
// QuerySelector
// pega somente o primeiro elemento
var ItensQuery1 = document.querySelector('#lista1 li');
console.log(ItensQuery1);

// QuerySelectorAll
// pega todos os elementos
var ItensQuery2 = document.querySelectorAll('li');
console.log(ItensQuery2);

// pegando somente os elementos da uma determinada classe
var ItensQuery3 = document.querySelectorAll('#lista2 li');
console.log(ItensQuery2);